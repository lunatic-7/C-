#include <iostream>

using namespace std;

int main()
{
    string charName = "Tom";
    int charAge;
    charAge = 35;
    cout << "There was a man named " << charName << endl;
    cout << "He was " << charAge << " years old." << endl;

    charName = "Mike";
    cout << "He liked the name " << charName << endl;
    cout << "But did not like being " << charAge << endl;
    return 0;
}